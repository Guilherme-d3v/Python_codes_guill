#definir uma matriz 3x3
matriz =[
    [1,2,3],
    [4,5,6],
    [7,8,9]
]

print(matriz)

#acessando a linha inteira
print("segunda linha matriz",matriz[1])

#acessando elementos especificos
print("elemento da terceira linha e primeira coluna",matriz [2][0])

#acessando o elemento da 2° linha e 3° coluna
elemento = matriz[1][2]
print(elemento)

